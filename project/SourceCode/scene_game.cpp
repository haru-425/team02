//----< インクルード >-----------------------------------------------------------
#include "all.h"

//------< 定数 >----------------------------------------------------------------


//------< 変数 >----------------------------------------------------------------
int game_state;
int game_timer;



//--------------------------------------
//  初期設定
//--------------------------------------
void game_init()
{
	game_state = 0;
	game_timer = 0;
}

//--------------------------------------
//  終了処理
//--------------------------------------
void game_deinit()
{

}

//--------------------------------------
//  更新処理
//--------------------------------------
void game_update()
{
	switch (game_state)
	{
	case 0:
		//////// 初期設定 ////////

		game_state++;
		/*fallthrough*/

	case 1:
		//////// パラメータの設定 ////////

		GameLib::setBlendMode(Blender::BS_ALPHA);

		game_state++;
		/*fallthrough*/

	case 2:
		//////// 通常時 ////////

		if (TRG(0) & PAD_SELECT)
		{
			nextScene = SCENE_TITLE;
			break;
		}


	}

	game_timer++;
}

//--------------------------------------
//  描画処理
//--------------------------------------
void game_render()
{
	GameLib::clear(0.2f, 0.2f, 0.4f);

}
